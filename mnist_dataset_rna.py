import numpy as np
import matplotlib.pyplot as plt
from tensorflow.keras.datasets import mnist
(imagens_treino, label_treino) , (imagens_teste, label_teste)=mnist.load_data()

imagens_treino.shape

np.set_printoptions(linewidth=120)
print(imagens_treino[0])
print("Label:",label_treino[0])

num_images = 7
plt.figure(figsize=(10, 4))
for i in range(num_images):
    plt.subplot(1, num_images, i + 1)
    plt.imshow(imagens_treino[i], cmap='gray')
    plt.title('Label: {}'.format(label_treino[i]))
    plt.axis('off')
plt.show()

#normalização dos dados
imagens_treino = imagens_treino.astype(float) /255
imagens_teste = imagens_teste.astype(float)/255

from tensorflow.keras.utils import to_categorical
label_treino = to_categorical(label_treino)
label_teste = to_categorical(label_teste)

print(label_treino[5])

#arquitetura da rede
from tensorflow.keras import layers, models
model = models.Sequential()
model.add(layers.Flatten(input_shape=(28, 28))) #entrada
model.add(layers.Dense(512, activation='tanh')) #oculta
model.add(layers.Dense(10, activation='softmax')) #saida

#config da rede
from tensorflow.keras.optimizers import Adam
algoritmo = Adam(learning_rate=0.001)
model.compile(optimizer=algoritmo,
              loss='categorical_crossentropy',
              metrics=['accuracy'])

#treinar a rede
clf = model.fit(imagens_treino, label_treino, epochs=5, batch_size=128)

teste_acuracia = model.evaluate(imagens_teste, label_teste)
print('Acurácia:', teste_acuracia)

